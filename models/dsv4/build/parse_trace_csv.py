#!/usr/bin/env python3
"""trace-kernel-learning/out/*.csv  →  arch-viewer/ir/profiles/sglang_v4_pro_{prefill,decode}.yaml

只做格式转换, 不做语义改动. 字段对应 schema.yaml 里的 profile.
"""

from __future__ import annotations

import csv
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parent.parent
TRACE_OUT = ROOT.parent / "trace-kernel-learning" / "out"
PROFILE_DIR = ROOT / "ir" / "profiles"
PROFILE_DIR.mkdir(parents=True, exist_ok=True)


def parse_csv(csv_path: Path) -> dict[str, dict[str, dict[str, Any]]]:
    """returns {stage_id -> {layer_type -> {ms_per_iter, kernels:[...]}}}"""
    by_stage: dict[str, dict[str, dict[str, Any]]] = defaultdict(
        lambda: defaultdict(lambda: {"total_us": 0.0, "kernels": []})
    )
    with csv_path.open() as fh:
        for row in csv.DictReader(fh):
            stage = row["stage"]
            ltype = row["layer_type"]
            entry = by_stage[stage][ltype]
            total_us = float(row["total_us"])
            entry["total_us"] += total_us
            entry["kernels"].append({
                "name": row["kernel_name"],
                "count": int(row["count"]),
                "avg_us": float(row["avg_us"]),
                "total_us": total_us,
                "share_in_stage_pct": float(row["share_in_stage_pct"]),
                "share_in_layer_pct": float(row["share_in_layer_pct"]),
            })

    out: dict[str, dict[str, dict[str, Any]]] = {}
    for stage, by_layer in by_stage.items():
        out[stage] = {}
        for ltype, agg in by_layer.items():
            agg["kernels"].sort(key=lambda k: -k["total_us"])
            top = agg["kernels"][:6]
            out[stage][ltype] = {
                "ms_per_iter": round(agg["total_us"] / 1000, 3),
                "kernels": top,
            }
    return out


def shorten_kernel_name(n: str) -> str:
    """trace 里的 kernel name 太长, 砍到 80 char 内, 保留 deep_gemm/nvjet/flash 关键字"""
    if len(n) <= 80:
        return n
    keywords = [
        "deep_gemm::sm100", "nvjet_", "flash_fwd_splitkv_mla", "fused_norm_rope",
        "rmsnorm", "deepseek_rope", "per_token_group_quant", "fused_store_flashmla",
        "moe_fused_gate", "mhc_pre", "mhc_post", "flash_c128", "flash_c4",
        "RMSNormKernel", "flash_fwd_mla_combine", "get_mla_metadata",
    ]
    for kw in keywords:
        if kw in n:
            return f"<...>{kw}<...>"
    return n[:77] + "..."


def to_yaml(data: dict[str, dict[str, dict[str, Any]]], meta: dict[str, str]) -> str:
    lines: list[str] = []
    lines.append("# AUTO-GENERATED by build/parse_trace_csv.py — DO NOT EDIT.")
    lines.append("# regenerate: python3 build/parse_trace_csv.py")
    lines.append("meta:")
    for k, v in meta.items():
        lines.append(f"  {k}: {v!r}" if isinstance(v, str) else f"  {k}: {v}")
    lines.append("data:")
    for stage in sorted(data):
        lines.append(f"  {stage}:")
        for ltype in sorted(data[stage]):
            d = data[stage][ltype]
            lines.append(f"    {ltype}:")
            lines.append(f"      ms_per_iter: {d['ms_per_iter']}")
            lines.append("      kernels:")
            for k in d["kernels"]:
                short = shorten_kernel_name(k["name"]).replace("'", "''")
                lines.append(
                    f"        - {{name: '{short}', count: {k['count']}, "
                    f"avg_us: {k['avg_us']}, total_us: {k['total_us']}, "
                    f"share_in_stage_pct: {k['share_in_stage_pct']}}}"
                )
    return "\n".join(lines) + "\n"


def main() -> int:
    pairs = [
        ("sglang_prefill_kernel_breakdown.csv", "sglang_v4_pro_prefill.yaml", "prefill"),
        ("sglang_decode_kernel_breakdown.csv",  "sglang_v4_pro_decode.yaml",  "decode"),
    ]
    for csv_name, yaml_name, phase in pairs:
        csv_path = TRACE_OUT / csv_name
        if not csv_path.exists():
            print(f"missing: {csv_path}", file=sys.stderr)
            continue
        data = parse_csv(csv_path)
        meta = {
            "source": "trace",
            "variant": "sglang",
            "config": "v4_pro",
            "phase": phase,
            "setup": "sglang dev branch (~Apr 30 2026), 4-iter trace, per-iter aggregated",
            "csv_origin": str(csv_path.relative_to(ROOT.parent)),
        }
        out_path = PROFILE_DIR / yaml_name
        out_path.write_text(to_yaml(data, meta))
        print(f"wrote {out_path}  ({sum(len(v) for v in data.values())} stages×layers)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
